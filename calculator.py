class Calculator:

    def __init__(self):
        pass

    def add(self, x, y):
        return x + y

    def sub(self, x, y):
        return x - y
    
    def mult(self, x, y):
        return x * y
    
    def power(self, x, y):
        return x ** y
    
    def squareroot(self, x, y=2):
        return x ** (1/y)

    

        

